#include<iostream>
#include<cmath>
using namespace std;
int main()
{
    cout << "The smallest integer not less than 9.2 is " << ceil(9.2) << endl;
    cout << "The smallest integer not less than -9.8 is " << ceil(-9.8) << endl;
    cout << "cos 0.0 is " << cos(0.0) << endl;
    cout << "e raise to power 1 is " << exp(1.0) << endl;
    cout << "e raise to power 2 is " << exp(2.0) << endl;
    cout << "The absolute value of 5.1 is " << fabs(5.1) << endl;
    cout << "The absolute value of 0.0 is " << fabs(0.0) << endl;
    cout << "The absolute value of -8.76 is " << fabs(-8.76) << endl;
    cout << "The largest number not greater than 9.2 is " << floor(9.2) << endl;
    cout << "The largest number not greater than -9.8 is " << floor(-9.8) << endl;
    cout << "Remainder of 2.6 and 1.2 is " << fmod(2.6,1.2) << endl;
    cout << "The natural logarithm of 10 is " << log10(10.0) << endl;
    cout << "The natural logarithm of 100 is " << log10(100.0) << endl;
    cout << "2 raise to power 7 is " << pow(2,7) << endl;
    cout << "9 raise to power 0.5 is " << pow(9,0.5) << endl;
    cout << "sin 0.0 is " << sin(0.0) << endl;
    cout << "Square root of 9 is " << sqrt(9.0) << endl;
    cout << "tan 0.0 is " << tan(0.0) << endl;
    return 0;
}
